        <h1>Política de Privacidade</h1>
        <p>Na Hod-s, privacidade e segurança são prioridades e nos comprometemos com a transparência do tratamento de dados pessoais dos nossos usuários/clientes. Por isso, esta Política de Privacidade estabelece como é feita a coleta, uso e transferência de informações de clientes ou outras pessoas que acessam ou usam nosso site.</p>
        
        <h2>1. Quais dados coletamos sobre você e para qual finalidade?</h2>
