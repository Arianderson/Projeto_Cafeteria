            <li><strong>Dados pessoais fornecidos pelo titular:</strong> Nome, e-mail, telefone, endereço, etc.</li>
            <li><strong>Dados pessoais coletados automaticamente:</strong> IP, localização geográfica, tipo de navegador, etc.</li>
        </ul>
        
        <h2>2. Como coletamos os seus dados?</h2>
        <p>Os dados pessoais são coletados nas seguintes situações:</p>
        <ul>
            <li>Quando você preenche formulários em nosso site;</li>
            <li>Quando você interage com nosso atendimento ao cliente;</li>
            <li>Quando você navega em nosso site.</li>
        </ul>
        
        <h2>3. Quais são os seus direitos?</h2>
        <p>Você tem direito a solicitar:</p>
        <ul>
            <li>Acesso aos dados pessoais que temos sobre você;</li>
            <li>Correção de dados incompletos, inexatos ou desatualizados;</li>
            <li>Anonimização, bloqueio ou eliminação de dados desnecessários, excessivos ou tratados em desconformidade com a legislação;</li>
            <li>Portabilidade dos dados a outro fornecedor de serviço ou produto;</li>
            <li>Eliminação dos dados pessoais tratados com o seu consentimento;</li>
            <li>Informação sobre as entidades públicas e privadas com as quais compartilhamos seus dados;</li>
            <li>Informação sobre a possibilidade de não fornecer consentimento e sobre as consequências da negativa;</li>
            <li>Revogação do consentimento.</li>
        </ul>
        
        <h2>4. Como e por quanto tempo seus dados serão armazenados?</h2>
        <p>Seus dados pessoais serão armazenados pelo período necessário para cumprir as finalidades para as quais foram coletados, ou conforme exigido por lei.</p>
        
